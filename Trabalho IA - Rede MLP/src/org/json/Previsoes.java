package org.json;

public class Previsoes {
	String signo;
	String amor;
	String saude;
	String dinheiro;
	String trabalho;

	public Previsoes(String signo, String amor, String saude, String dinheiro, String trabalho) {
		this.signo = signo;
		this.amor = amor;
		this.saude = saude;
		this.dinheiro = dinheiro;
		this.trabalho = trabalho;
	}

}
